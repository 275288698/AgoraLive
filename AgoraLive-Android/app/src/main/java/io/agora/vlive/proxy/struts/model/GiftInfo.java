package io.agora.vlive.proxy.struts.model;

public class GiftInfo {
    String giftId;
    String giftName;
    String thumbnail;
    String animation;
    int points;

    public String getGiftId() {
        return giftId;
    }

    public String getGiftName() {
        return giftName;
    }

    public String getThumbnail() {
        return thumbnail;
    }

    public String getAnimation() {
        return animation;
    }

    public int getPoints() {
        return points;
    }
}
